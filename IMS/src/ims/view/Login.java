    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            User user = LoginController.getUser(txtUserName.getText().trim());
            if(user.getType().equals("")){
                JOptionPane.showMessageDialog(null, "User not found");
                txtUserName.setText("");
                pwdPassword.setText("");
           }else{
                String password = user.getPassword();
                String type = user.getType();
                if(password.equals(new String(pwdPassword.getPassword()))){
                    switch(type){
                        case "Admin": new ManagerMain().setVisible(true);
                                      this.dispose();
                                            break;
                        case "Manager": new ManagerMain().setVisible(true);
                                        this.dispose();
                                            break;
                        case "Store keeper": break;
                        case "Cashier": break;
                    }
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

